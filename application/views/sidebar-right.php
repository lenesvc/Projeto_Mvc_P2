					<h1 class="page-title">Handle - Produto</h1>
				</header>
				<p>Handle é um apoio de mão para os passageiros de meio de transporte público (tais como,ônibus,metrô e trem) que estão viajando em pé.</p>
				<p>Estabilidade pelo sistema de cinto de dupla ligação sem falhas que suporta peso de até 500 kg proporcionando segurança ao usuário – Homologado pelo Instituto Pesquisas Tecnologicas,aprovado pelo INMETRO e autorizado pelo SPTrans.Fabricado no tamanho adequado para dar conforto ao passageiro: 80 mm de engate e 160 mm de pêndulo, totalizando 240 mm de altura.</p>
				<p>Segurança, resistência e conforto já difundida em várias cidades como Hong Kong, ShenZen,Beijing, Xangai, Taiwan, entre outras cidades da Ásia. Serão instalados os Handles (apoio de mão) em diversas linhas de ônibus, metrô e trem de forma
				para que a população o utilize como forma de apoio no sistema de transporte público. </p>

